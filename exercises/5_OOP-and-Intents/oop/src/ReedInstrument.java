public abstract class ReedInstrument implements Instrument {

    boolean reed;
    public boolean has_reed(Clarinet c){
        if (c.reed = true)
        return true;
        else
            return false;
    }

    public void remove_reed() {
        if (reed = true){
            reed = false;
        }
    }



}
